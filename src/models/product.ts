import mongoose, { Schema } from "mongoose";
const ProductSchema = mongoose.Schema;

const productSchema: Schema = new ProductSchema({
  title: {
    type: String,
    required: true,
  },
  imageUrl: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: "User",
    required: true,
  },
});

export = mongoose.model("Product", productSchema);
